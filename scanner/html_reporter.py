"""Render self-contained HTML reports from a ScanReport / ComparisonReport.

The output is a single .html file with inline CSS (no external assets), so it
opens anywhere and is easy to screenshot for a portfolio. A dark "terminal"
theme is used on purpose.

Security note: every piece of model/attack content is HTML-escaped before it is
embedded. A target's response is untrusted input — it could contain `<script>`
or other markup — and the report must not become an XSS vector when opened in a
browser. This mirrors the prompt-injection-safe stance of the Claude judge.
"""

from __future__ import annotations

from html import escape as esc

from .models import ComparisonReport, ScanReport, Verdict

